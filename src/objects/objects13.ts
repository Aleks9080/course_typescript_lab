export const user = {
    name: 'Алексей',
    greeting() {
        // обычная функция теряет this при вызове без контекста,
        // поэтому используем стрелочную — она берёт this из внешней области (greeting)
        const externalFn = () => {
            console.log(`Привет, меня зовут ${this.name}`);
        };
        externalFn();
    }
};